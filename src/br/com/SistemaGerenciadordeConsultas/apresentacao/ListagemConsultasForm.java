    private void excluirConsulta() throws SQLException {

        try {
            int linhaSelecionada = tblConsulta.getSelectedRow();
            if (linhaSelecionada != -1) {
                Consulta consultaSelecionado = consultas.get(linhaSelecionada);

                int resposta;
                String mensagem = "Deseja excluir consulta marcada para : " + consultaSelecionado.getPaciente() + "?";
                String titulo = "ExclusÃ£o de Consulta";
                resposta = JOptionPane.showConfirmDialog(this,
                        mensagem, titulo, JOptionPane.YES_NO_OPTION);

                if (resposta == JOptionPane.YES_NO_OPTION) {
                    ConsultaBO consultaBO = new ConsultaBO();
                    consultaBO.removerConsulta(consultaSelecionado.getId());
                    JOptionPane.showMessageDialog(this,
                            "Consulta Excluida com sucesso!",
                            "ExclusÃ£o de Consulta", JOptionPane.INFORMATION_MESSAGE);
                    this.carregarTabela();
                }

            } else {
                String mensagem = "Nenhuma consulta selecionada para Excluir.";
                JOptionPane.showMessageDialog(this,
                        mensagem, "ExclusÃ£o de consulta",
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            String mensagem = "Erro inesperado! Informe a mensagem de erro ao administrador do sistema.";
            mensagem += "\nMensagem de erro:\n" + e.getMessage();
            JOptionPane.showMessageDialog(this,
                    mensagem, "ExclusÃ£o de Consulta ",
                    JOptionPane.ERROR_MESSAGE);
            this.dispose();

        }
    }

    public void carregarTabela() {
        ConsultaBO consultaBO = new ConsultaBO();
        try {
            this.consultas = consultaBO.buscarTodos();
        } catch (SQLException e) {
            e.printStackTrace();
        }
        Model model = new Model();
        this.tblConsulta.setModel(model);
    }

    private class Model extends AbstractTableModel {

        @Override
        public int getRowCount() {
            return consultas.size();
        }

        @Override
        public int getColumnCount() {
            return 5;
        }

        @Override
        public Object getValueAt(int linha, int coluna) {
            Consulta consulta = consultas.get(linha);
            if (coluna == 0) {
                return ConverteData.DateForString(consulta.getData());
            } else if (coluna == 1) {
                return consulta.getHorario();
            } else if (coluna == 2) {
                return consulta.getMedico().getNome();
            } else if (coluna == 3) {
                return consulta.getPaciente().getNome();
            } else if (coluna == 4) {
                return consulta.getObservacao();
            }
            return null;
        }

        @Override
        public String getColumnName(int coluna) {
            if (coluna == 0) {
                return "DATA";
            } else if (coluna == 1) {
                return "HORÃRIO";
            } else if (coluna == 2) {
                return "MÃDICO";
            } else if (coluna == 3) {
                return "PACIENTE";
            } else if (coluna == 4) {
                return "OBSERVACAO";
            }
            return null;
        }
    }
